// How many ways we can define the function

// 1. Classical way 1995 to 2023
//1. Function Defination
// Function Body
function aFunction(){
    console.log('I am A Function')
    // Every function return something
}
// 2. Function calling/Invoking
aFunction();


// 2. New way 2015 ES6
// Fat arrow function
// 1. Function Defination
var bFunction = () => {
    // Function Body
    console.log("I am B Function")
}
// 2. Function calling/invoking
bFunction();